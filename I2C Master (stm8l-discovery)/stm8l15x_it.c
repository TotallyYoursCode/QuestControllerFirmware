/**
  * @brief I2C1 / SPI2 Interrupt routine.
  * @param  None
  * @retval None
  */
typedef enum{TRANSMITTER, RECEIVER}i2c_master_mode_t;
i2c_master_mode_t mode;
static uint8_t *i2c_buf, bytes, complete=1;

void I2C_transmit_buffer(uint8_t *buf, uint8_t len){
  i2c_buf = buf;
  bytes = len;
  complete = 0;
  mode = TRANSMITTER;
  while(I2C_GetFlagStatus(I2C1, I2C_FLAG_BUSY));
  I2C_GenerateSTART(I2C1, ENABLE);  
  while(!complete);
}

void I2C_receive_buffer(uint8_t *buf, uint8_t len){
  i2c_buf = buf;
  bytes = len;
  complete = 0;
  mode = RECEIVER;
  while(I2C_GetFlagStatus(I2C1, I2C_FLAG_BUSY));
  I2C_GenerateSTART(I2C1, ENABLE);
  while(!complete);
}


