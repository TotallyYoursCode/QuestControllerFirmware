#include "interboards_comm.h"
static uint8_t I2C_Buf[sizeof(EXT_BOARD_PACKET_T)], *pI2C_Buf, Bytes, Complete = 0;

uint8_t I2C_get_data(uint8_t * buf){
  if(Complete){
    pI2C_Buf = I2C_Buf;
    while(Bytes--){
      *buf++ = *pI2C_Buf++;
    }
    Complete = 0;    
    return 1;
  } else {
    return 0;
  }
}

 
